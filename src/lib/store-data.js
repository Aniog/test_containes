export function getProductBySlug(slug) {
  return products.find((product) => product.slug === slug)
}

export function getRelatedProducts(slug, limit = 4) {
  const currentProduct = getProductBySlug(slug)

  if (!currentProduct) {
    return products.slice(0, limit)
  }

  const sameCategory = products.filter(
    (product) => product.slug !== slug && product.category === currentProduct.category,
  )
  const fallback = products.filter((product) => product.slug !== slug)

  return [...sameCategory, ...fallback].slice(0, limit)
}
