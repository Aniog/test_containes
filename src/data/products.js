export const getProductBySlug = (slug) => PRODUCTS.find((product) => product.slug === slug)

export const getRelatedProducts = (slug) => {
  const current = getProductBySlug(slug)

  if (!current) {
    return PRODUCTS.slice(0, 4)
  }

  const sameCategory = PRODUCTS.filter(
    (product) => product.slug !== slug && product.category === current.category,
  )
  const fallback = PRODUCTS.filter((product) => product.slug !== slug)

  return [...sameCategory, ...fallback].slice(0, 4)
}
