export const getProductById = (id) => products.find((p) => p.id === id);

export const getRelatedProducts = (productId, limit = 4) => {
  const current = getProductById(productId);
  if (!current) return [];
  return products
    .filter((p) => p.id !== productId && p.category === current.category)
    .slice(0, limit);
};
