export const getRelated = (product, count = 4) =>
  PRODUCTS.filter((p) => p.id !== product.id)
    .sort((a, b) => {
      const aScore = a.category === product.category ? 0 : 1;
      const bScore = b.category === product.category ? 0 : 1;
      return aScore - bScore;
    })
    .slice(0, count);
