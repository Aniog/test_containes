export const getProductBySlug = (slug) => products.find((product) => product.slug === slug)

export const getRelatedProducts = (slug) => {
  const currentProduct = getProductBySlug(slug)

  if (!currentProduct) {
    return products.slice(0, 4)
  }

  return products
    .filter((product) => product.slug !== slug)
    .sort((first, second) => Number(second.category === currentProduct.category) - Number(first.category === currentProduct.category))
    .slice(0, 4)
}

