export const formatCurrency = (value) => `$${value}`

export const getProductBySlug = (slug) =>
  products.find((product) => product.slug === slug)

export const getRelatedProducts = (slug) => {
  const activeProduct = getProductBySlug(slug)

  if (!activeProduct) {
    return products.slice(0, 3)
  }

  return products
    .filter(
      (product) =>
        product.slug !== slug &&
        (product.category === activeProduct.category ||
          product.materials.some((material) => activeProduct.materials.includes(material))),
    )
    .slice(0, 3)
}

export const sortProducts = (list, sortValue) => {
  const nextList = [...list]

  if (sortValue === 'price-asc') {
    return nextList.sort((a, b) => a.price - b.price)
  }

  if (sortValue === 'price-desc') {
    return nextList.sort((a, b) => b.price - a.price)
  }

  if (sortValue === 'rating') {
    return nextList.sort((a, b) => b.rating - a.rating)
  }

  if (sortValue === 'name') {
    return nextList.sort((a, b) => a.name.localeCompare(b.name))
  }

  return nextList
}

export const filterProducts = (list, filters) => {
  return list.filter((product) => {
    const matchesCategory =
      filters.categories.length === 0 || filters.categories.includes(product.category)

    const matchesMaterial =
      filters.materials.length === 0 ||
      filters.materials.some((material) => product.materials.includes(material))

    const matchesPrice =
      filters.price === '' ||
      (filters.price === 'under-50' && product.price < 50) ||
      (filters.price === '50-80' && product.price >= 50 && product.price <= 80) ||
      (filters.price === '80-plus' && product.price > 80)

    return matchesCategory && matchesMaterial && matchesPrice
  })
}
