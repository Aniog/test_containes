export const getRelatedProducts = (currentProduct) =>
  products
    .filter(
      (product) =>
        product.slug !== currentProduct.slug &&
        (product.category === currentProduct.category ||
          product.collection === currentProduct.collection),
    )
    .slice(0, 3)
