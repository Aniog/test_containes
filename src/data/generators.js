/**
 * Return generators grouped by category id.
 */
export function getGeneratorsByCategory() {
  const grouped = {};
  for (const cat of categories) {
    grouped[cat.id] = generators.filter((g) => g.category === cat.id);
  }
  return grouped;
}

/**
 * Search generators across name, description, and category.
 */
export function searchGenerators(query) {
  if (!query || !query.trim()) return generators;
  const q = query.toLowerCase().trim();
  return generators.filter(
    (g) =>
      g.name.toLowerCase().includes(q) ||
      g.description.toLowerCase().includes(q) ||
      g.category.toLowerCase().includes(q)
  );
}
