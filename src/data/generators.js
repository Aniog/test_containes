export function getAllGenerators() {
  const all = [];
  Object.entries(generatorsByCategory).forEach(([catId, gens]) => {
    const cat = categories.find((c) => c.id === catId);
    gens.forEach((g) => {
      all.push({
        ...g,
        slug: slugify(g.name),
        categoryId: catId,
        categoryName: cat?.name || '',
        categoryTag: cat?.shortTag || '',
      });
    });
  });
  return all;
}

export function getGeneratorsByCategory(catId) {
  const cat = categories.find((c) => c.id === catId);
  return (generatorsByCategory[catId] || []).map((g) => ({
    ...g,
    slug: slugify(g.name),
    categoryId: catId,
    categoryName: cat?.name || '',
    categoryTag: cat?.shortTag || '',
  }));
}

