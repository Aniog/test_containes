// Derived stats
export function getStats(records) {
  const highs = records.map((r) => r.high);
  const lows = records.map((r) => r.low);
  const humidities = records.map((r) => r.humidity);
  const totalPrecip = records.reduce((sum, r) => sum + r.precipitation, 0);
  const avgWind = records.reduce((sum, r) => sum + r.wind_speed, 0) / records.length;

  return {
    maxHigh: Math.max(...highs),
    minLow: Math.min(...lows),
    avgHigh: Math.round(highs.reduce((a, b) => a + b, 0) / highs.length),
    avgLow: Math.round(lows.reduce((a, b) => a + b, 0) / lows.length),
    avgHumidity: Math.round(humidities.reduce((a, b) => a + b, 0) / humidities.length),
    totalPrecip: totalPrecip.toFixed(1),
    avgWind: Math.round(avgWind),
    rainyDays: records.filter((r) => r.precipitation > 0).length,
    sunnyDays: records.filter((r) => r.condition === "Sunny").length,
  };
}
