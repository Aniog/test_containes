export const getMemoryById = (id) => memories.find(m => m.id === id);

export const getFilteredMemories = ({ era, emotion, lifeEvent, region, search }) => {
  return memories.filter(m => {
    if (era && m.era !== era) return false;
    if (emotion && m.emotion !== emotion) return false;
    if (lifeEvent && m.lifeEvent !== lifeEvent) return false;
    if (region && m.region !== region) return false;
    if (search) {
      const q = search.toLowerCase();
      return (
        m.title.toLowerCase().includes(q) ||
        m.excerpt.toLowerCase().includes(q) ||
        m.contributor.toLowerCase().includes(q) ||
        m.location.toLowerCase().includes(q) ||
        m.tags.some(t => t.toLowerCase().includes(q))
      );
    }
    return true;
  });
};

